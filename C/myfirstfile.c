#include <stdio.h>
    
int main (){         //this is called function

    printf("Hello World!\n");     // this function is used to output print on screen.
    printf("my name is\n ");        // \n used for new line

    printf("Our city is surat\n");        
    printf("Our city is surat\t\n");          // \t for horizontal space 
    printf("Our city is surat\\\n");          // \\ for insert backlash character
    printf("Our city is \"surat\"\n");        // \" for insert word in double comma. 
    printf("Our city is surat\n");

    return 0;                   // this is end the main function.
}
